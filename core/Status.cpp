#include "Status.h"

namespace Grape
{

//=============================================================================
std::ostringstream& Status::set(long long int code)
//=============================================================================
{
    clear();
    _code = code;
    return _stream;
}

//-----------------------------------------------------------------------------
void Status::clear()
//-----------------------------------------------------------------------------
{
    _code = 0;
    _stream.clear();
    _stream.str(std::string());
}

} // Grape
